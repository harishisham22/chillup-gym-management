package bean;

/**
 *
 * @author MOHAMMAD NORHARIS
 */
public class adminBean implements java.io.Serializable{
    private String fullname, email, phone, username, password;
    
    public adminBean(){
        fullname = email = phone = username = password = null;
    }

    public adminBean(String fullname, String email, String phone, String username, String password) {
        this.fullname = fullname;
        this.email = email;
        this.phone = phone;
        this.username = username;
        this.password = password;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
